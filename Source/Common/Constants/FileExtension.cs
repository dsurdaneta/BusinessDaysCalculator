namespace DsuDev.BusinessDays.Common.Constants
{
    /// <summary>
    /// Class to maintain file extensions constants, as if it was an Enum
    /// </summary>
    public static class FileExtension
    {
        /// <summary>
        /// The JSON file extension
        /// </summary>
        public const string Json = "json";
        /// <summary>
        /// The XML file extension
        /// </summary>
        public const string Xml = "xml";
        /// <summary>
        /// The CSV file extension with headers and separated by ;
        /// </summary>
        public const string Csv = "csv";
        /// <summary>
        /// the TXT file extension, for Custom protocols
        /// </summary>
        public const string Txt = "txt";
    }
}
